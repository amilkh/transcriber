IDLE_TRIGGER_SECS  = int(os.getenv("IDLE_TRIGGER_SECS", str(10 * 60)))  # 10 min

_active_sessions      = 0
_active_sessions_lock = Lock()
_idle_timer: threading.Timer | None = None


def _session_started() -> None:
    global _active_sessions, _idle_timer
    with _active_sessions_lock:
        _active_sessions += 1
        if _idle_timer is not None:
            _idle_timer.cancel()
            _idle_timer = None


def _session_ended() -> None:
    global _active_sessions, _idle_timer
    with _active_sessions_lock:
        _active_sessions = max(0, _active_sessions - 1)
        if _active_sessions == 0 and AUTO_IMPROVE and os.path.isfile(_IMPROVE_SCRIPT):
            _idle_timer = threading.Timer(IDLE_TRIGGER_SECS, _run_idle_improve)
            _idle_timer.daemon = True
            _idle_timer.start()
            logger.info("No active sessions — will run idle_improve.py in %ds", IDLE_TRIGGER_SECS)


def _run_idle_improve() -> None:
    venv_python = os.path.join(_REPO_ROOT, ".venv", "bin", "python3")
    python = venv_python if os.path.isfile(venv_python) else "python3"
    try:
        logger.info("Idle: launching %s", _IMPROVE_SCRIPT)
        subprocess.Popen(
            [python, _IMPROVE_SCRIPT],
            cwd=_REPO_ROOT,
            stdout=open("/tmp/idle_improve.log", "a"),
            stderr=subprocess.STDOUT,
        )
    except Exception as exc:
        logger.warning("idle_improve launch failed: %s", exc)

